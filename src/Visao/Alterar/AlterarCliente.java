    private void btOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOKActionPerformed
        // TODO add your handling code here:
        String codigo = codCliente.getText();
        Connection con = Conexao.AbrirConexao();
        ClienteDAO sql = new ClienteDAO(con);
        int cod = Integer.parseInt(codigo);
        
        if(sql.Testar_Cliente(cod) == false){
            JOptionPane.showMessageDialog(null, "Código não Encontrado no Banco", 
                    "LocVideo", JOptionPane.ERROR_MESSAGE);
            Conexao.FecharConexao(con);
        }
        
        if(codigo.equals("")){
            JOptionPane.showMessageDialog(null, "Digite um Código para Atualizar", 
                    "LocVideo", JOptionPane.WARNING_MESSAGE);
        }
        
        tfCodCliente.setText("");
        tfNome.setText("");
        ftfCEP.setText("");
        tfNumero.setText("");
        tfBairro.setText("");
        tfEmail.setText("");
        ftfTelefone.setText("");
        tfRua.setText("");
        ftfDataNasc.setText("");
        tfRG.setText("");
        ftfCPF.setText("");
        
        InserirDados(cod);
        codCliente.setText("");
        
    }//GEN-LAST:event_btOKActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        String codigo = tfCodCliente.getText();
        String nome = tfNome.getText();
        String nascimento = ftfDataNasc.getText();
        String cep = ftfCEP.getText();
        String rua = tfRua.getText();
        String numero = tfNumero.getText();
        String bairro = tfBairro.getText();
        String email = tfEmail.getText();
        String fone = ftfTelefone.getText();
        String cpf = ftfCPF.getText();
        String rg = tfRG.getText();
        
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio.", 
                    "LocVideo", JOptionPane.WARNING_MESSAGE);
        }else{
            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            int num = Integer.parseInt(numero);
            int cod = Integer.parseInt(codigo);
            Cliente a = new Cliente();
            
            a.setCodigo(cod);
            a.setNome(nome);
            a.setNascimento(nascimento);
            a.setRG(rg);
            a.setCPF(cpf);
            a.setNumero(num);
            a.setBairro(bairro);
            a.setCEP(cep);
            a.setRua(rua);
            a.setEmail(email);
            a.setTelefone(fone);
            
            sql.Alterar_Cliente(a);
            Conexao.FecharConexao(con);
            
            tfNome.setText("");
            ftfCEP.setText("");
            tfNumero.setText("");
            tfBairro.setText("");
            tfEmail.setText("");
            ftfTelefone.setText("");
            tfRua.setText("");
            ftfDataNasc.setText("");
            tfRG.setText("");
            ftfCPF.setText("");
            JOptionPane.showMessageDialog(null, "Cadastro Realizado com Sucesso.", 
                    "LocVideo", JOptionPane.INFORMATION_MESSAGE);
            
            dispose();
            new AlterarCliente().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btAlterarActionPerformed

